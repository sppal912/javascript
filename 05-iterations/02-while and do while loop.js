// while loop

let index = 0
while (index <= 10) {
    console.log(`Value of index is ${index}`)
    index = index + 1
}

const superHero = ['souvik', 'sourav', 'surojit', 'shovon']
let index1 = 0
while (index1 < superHero.length ) {
    console.log(`value is  ${superHero[index1]}`)
    index1 = index1 + 1
}

// do while loop

let score = 1
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);